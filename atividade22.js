/*22 - Faça um algoritmo que leia dois valores inteiros A e B, imprima na tela o quociente e o
resto da divisão inteira entre eles.
 */

let valorA = 10, valorB = 2, quociente, resto;

quociente = valorA / valorB;
resto = valorA % valorB;

console.log('Quociente: '+quociente+'\nResto: '+resto);